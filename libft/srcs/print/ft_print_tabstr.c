#include "libft.h"

void	ft_print_tabstr(char **tab)
{
	int	i;

	i = 0;
	if (!tab)
		return ;
	while (tab[i])
	{
		ft_printf("%d -> %s\n", i, tab[i]);
		i++;
	}
}
