#include "libft.h"

int	ft_isalpha(int c)
{
	return ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'));
}

bool	ft_is_upper(int c)
{
	if (c >= 'A' && c <= 'Z')
		return (true);
	return (false);
}
